from __future__ import annotations

from pathlib import Path

from reportlab.lib import colors
from reportlab.lib.enums import TA_CENTER, TA_JUSTIFY, TA_LEFT
from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import ParagraphStyle, getSampleStyleSheet
from reportlab.lib.units import cm
from reportlab.pdfbase import pdfmetrics
from reportlab.pdfbase.ttfonts import TTFont
from reportlab.platypus import Paragraph, SimpleDocTemplate, Spacer


ROOT = Path(__file__).resolve().parents[1]
OUTPUT = ROOT / "docs" / "scenario_usage_bot.pdf"

FONT_REGULAR = r"C:\Windows\Fonts\arial.ttf"
FONT_BOLD = r"C:\Windows\Fonts\arialbd.ttf"


def p(text: str, style: ParagraphStyle) -> Paragraph:
    return Paragraph(text.replace("\n", "<br/>"), style)


def main() -> None:
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    pdfmetrics.registerFont(TTFont("Arial", FONT_REGULAR))
    pdfmetrics.registerFont(TTFont("Arial-Bold", FONT_BOLD))

    styles = getSampleStyleSheet()
    title = ParagraphStyle(
        "TitleRu",
        parent=styles["Title"],
        fontName="Arial-Bold",
        fontSize=17,
        leading=20,
        alignment=TA_CENTER,
        spaceAfter=12,
    )
    subtitle = ParagraphStyle(
        "SubtitleRu",
        parent=styles["Normal"],
        fontName="Arial",
        fontSize=9.5,
        leading=12.2,
        alignment=TA_CENTER,
        textColor=colors.HexColor("#555555"),
        spaceAfter=16,
    )
    heading = ParagraphStyle(
        "HeadingRu",
        parent=styles["Heading2"],
        fontName="Arial-Bold",
        fontSize=11.4,
        leading=13,
        spaceBefore=5,
        spaceAfter=3,
        textColor=colors.HexColor("#1f1f1f"),
    )
    body = ParagraphStyle(
        "BodyRu",
        parent=styles["BodyText"],
        fontName="Arial",
        fontSize=9.1,
        leading=11.45,
        alignment=TA_JUSTIFY,
        firstLineIndent=0.55 * cm,
        spaceAfter=6,
    )
    body_no_indent = ParagraphStyle(
        "BodyNoIndentRu",
        parent=body,
        firstLineIndent=0,
        alignment=TA_LEFT,
    )

    story = [
        p("Сценарий работы Telegram-бота для обучения", title),
        p(
            "Простое описание того, как человек пользуется ботом: от первого запуска до готового "
            "плана обучения и прохождения заданий.",
            subtitle,
        ),
        p("1. Человек открывает бота", heading),
        p(
            "Пользователь заходит в Telegram-бота и нажимает /start. Бот здоровается и коротко "
            "объясняет, зачем он нужен: он помогает человеку составить личный план обучения под "
            "конкретную цель. Например, человек хочет стать frontend-разработчиком, дизайнером, "
            "backend-разработчиком или просто освоить новый навык.",
            body,
        ),
        p(
            "После приветствия у пользователя есть две понятные кнопки: «Сделать трек» и "
            "«Информация». Если нажать «Информация», бот простыми словами рассказывает, что он "
            "подбирает темы, материалы и задания. Если нажать «Сделать трек», начинается создание "
            "личного учебного маршрута.",
            body,
        ),
        p("2. Бот задает несколько простых вопросов", heading),
        p(
            "Сначала бот спрашивает цель: чему пользователь хочет научиться. Пользователь может "
            "написать обычным текстом, например: «Хочу стать Python-разработчиком» или «Хочу "
            "научиться делать сайты». Если цель слишком общая, бот уточняет направление. Например, "
            "для Python он может спросить: backend, Telegram-боты, Data Science или автоматизация.",
            body,
        ),
        p(
            "Дальше бот спрашивает уровень пользователя: новичок, есть базовые знания или уже есть "
            "опыт. Потом он спрашивает, сколько времени человек готов учиться в неделю: до 3 часов, "
            "от 3 до 7 часов или больше 7 часов. После этого бот уточняет, какой формат удобнее: "
            "видео, статьи, практика или смешанный вариант.",
            body,
        ),
        p(
            "Еще бот может спросить пожелания: например, больше практических заданий, без длинных "
            "видео, больше коротких материалов или не показывать определенный источник. Важно: если "
            "пользователь уже написал часть информации в первом сообщении, бот не мучает его "
            "повторными вопросами, а сразу учитывает эти ответы.",
            body,
        ),
        p("3. Бот создает личный план обучения", heading),
        p(
            "Когда бот собрал все нужные ответы, он пишет что-то вроде: «Пожалуйста, подождите, "
            "создаю твой трек». После этого бот составляет понятный учебный план. В плане есть "
            "темы по порядку, примерная длительность обучения, материалы для изучения, практические "
            "задания и маленькие проверки, чтобы человек понял, что он действительно разобрался.",
            body,
        ),
        p(
            "Например, если пользователь хочет стать frontend-разработчиком, бот может построить "
            "маршрут из блоков: HTML и CSS, JavaScript, React, практика, портфолио. После создания "
            "плана бот отправляет сообщение: «Твой план успешно создан», показывает примерную "
            "длительность и основные темы.",
            body,
        ),
        p("4. Пользователь открывает карту обучения", heading),
        p(
            "После создания трека у пользователя появляются кнопки: «Открыть карту», «Профиль» и "
            "«AI-мастер». Кнопка «Открыть карту» открывает мини-приложение внутри Telegram. Там "
            "человек видит свой путь как карту: какие шаги уже пройдены, какие сейчас в работе, "
            "а какие еще впереди.",
            body,
        ),
        p(
            "Каждый шаг можно открыть. Внутри шага есть тема, ссылка на материал, задание и вопросы "
            "для самопроверки. Пользователь изучает материал, выполняет задание и нажимает, что шаг "
            "пройден. За это он получает XP, у него растет прогресс и сохраняется серия дней обучения.",
            body,
        ),
        p("5. Мини-проверка знаний", heading),
        p(
            "До мини-проверки пользователь доходит через карту обучения. Он открывает шаг, например "
            "«Основы JavaScript», изучает предложенный материал и выполняет задание. После этого "
            "появляется кнопка вроде «Проверить знания» или «Завершить шаг». Это не отдельный экзамен, "
            "а короткая проверка прямо внутри текущего шага.",
            body,
        ),
        p(
            "Мини-проверка нужна, чтобы бот понял: человек не просто нажал кнопку, а действительно "
            "что-то сделал. Она может выглядеть по-разному. Первый вариант — несколько простых "
            "вопросов по теме. Например: «Чем div отличается от span?» или «Для чего нужен useState "
            "в React?». Второй вариант — открытый ответ, где пользователь своими словами объясняет "
            "тему. Третий вариант — маленькая практика: написать кусок кода, составить чеклист или "
            "описать результат выполненного задания.",
            body,
        ),
        p(
            "Если пользователь отвечает нормально, бот засчитывает шаг как пройденный, начисляет XP "
            "и обновляет прогресс по навыку. Если ответ слабый или человек ошибся, бот не ругает его, "
            "а объясняет, что стоит повторить, и предлагает вернуться к материалу или попробовать "
            "еще раз. Так мини-проверка становится не наказанием, а способом закрепить знания.",
            body,
        ),
        p(
            "Для жюри это важный момент: в проекте есть не только выдача учебного плана, но и контроль "
            "прохождения. Пользователь получает награду не за случайный клик, а за понятное действие: "
            "изучил тему, выполнил задание, прошел мини-проверку и только после этого получил XP.",
            body,
        ),
        p("6. Если план не подошел, его можно исправить", heading),
        p(
            "Если человеку не нравится часть плана, он может нажать «Скорректировать». Например, "
            "он может написать: «Первый блок я уже знаю», «Слишком сложно», «Хочу больше практики» "
            "или «Не хочу длинные видео». После этого бот меняет отдельные шаги, а не заставляет "
            "начинать все заново.",
            body,
        ),
        p("7. Что происходит в разделе «Профиль»", heading),
        p(
            "После создания учебного трека у пользователя в меню появляется кнопка «Профиль». Если "
            "он нажимает на нее, открывается личная страница внутри мини-приложения. Это место, где "
            "человек видит не сами уроки, а свой общий результат: кто он в системе, какую цель выбрал "
            "и насколько продвинулся.",
            body,
        ),
        p(
            "В профиле отображается имя пользователя, текущий уровень, количество XP, streak, "
            "прокоины, выбранная цель обучения и примерное время до завершения маршрута. Например, "
            "пользователь может увидеть: цель — «Стать frontend-разработчиком», уровень — 3, XP — "
            "420 из 600 до следующего уровня, streak — 5 дней подряд.",
            body,
        ),
        p(
            "Ниже в профиле показываются навыки и прогресс по ним. Например: HTML/CSS — 60%, "
            "JavaScript — 35%, React — 10%. Это нужно, чтобы пользователь понимал, в чем он уже "
            "разобрался, а что еще слабое. Для жюри это важная часть сценария: бот не просто выдает "
            "план, а показывает рост человека по конкретным навыкам.",
            body,
        ),
        p(
            "Также в профиле есть блок «Что дальше?». Он подсказывает простые действия: продолжить "
            "изучать новые темы, практиковаться каждый день и двигаться к цели. То есть профиль "
            "работает как личный кабинет ученика: здесь видно мотивацию, прогресс и следующий смысл "
            "действий.",
            body,
        ),
        p("8. Сценарий XP и прогресса", heading),
        p(
            "XP — это очки опыта, которые пользователь получает за реальные действия в обучении. "
            "Бот не должен выдавать полный XP просто за кнопку «Готово». Пользователь получает очки, "
            "когда он действительно прошел шаг: изучил материал, ответил на вопрос самопроверки, "
            "выполнил практическое задание или написал короткий результат своей работы.",
            body,
        ),
        p(
            "Пример: пользователь открыл шаг «Основы HTML и CSS», прочитал материал и выполнил "
            "задание — сверстал простую карточку профиля. После этого бот задает мини-вопросы или "
            "просит подтвердить результат. Если все нормально, шаг считается пройденным, пользователю "
            "начисляется XP, а прогресс по навыку «HTML/CSS» становится выше.",
            body,
        ),
        p(
            "XP нужен не просто для красоты. Он показывает, что человек двигается вперед. Когда XP "
            "становится больше, пользователь повышает уровень в профиле. Если он занимается несколько "
            "дней подряд, у него растет streak — серия активных дней. Это мотивирует возвращаться к "
            "обучению и не бросать трек после первого дня.",
            body,
        ),
        p(
            "Если пользователь пропускает шаг, XP за него не начисляется. Если он возвращается к "
            "пропущенному шагу и проходит его нормально, бот снова может засчитать прогресс. Так "
            "система остается честной: награда дается не за случайные клики, а за понятные учебные "
            "действия.",
            body,
        ),
        p("9. AI-мастер помогает во время обучения", heading),
        p(
            "AI-мастер нужен как помощник рядом с пользователем. Ему можно задать вопрос по текущей "
            "теме, попросить объяснить проще, попросить пример или узнать, что делать дальше. Он "
            "отвечает не абстрактно, а с учетом цели пользователя и его текущего плана.",
            body,
        ),
        p(
            "Также бот может присылать мотивационные напоминания: вернуться к обучению, закрыть "
            "следующий шаг или пройти мини-проверку. Но уведомления не должны раздражать: их нельзя "
            "отправлять ночью, слишком часто или если пользователь их отключил.",
            body,
        ),
        Spacer(1, 8),
        p(
            "Итог сценария: человек открывает бота, отвечает на несколько простых вопросов и получает "
            "готовый личный план обучения. Дальше он проходит шаги, получает XP, задает вопросы "
            "AI-мастеру и при необходимости меняет план под себя.",
            body_no_indent,
        ),
    ]

    doc = SimpleDocTemplate(
        str(OUTPUT),
        pagesize=A4,
        rightMargin=1.35 * cm,
        leftMargin=1.35 * cm,
        topMargin=1.25 * cm,
        bottomMargin=1.25 * cm,
        title="Сценарий использования образовательного Telegram-бота",
        author="MTC ChatBot",
    )
    doc.build(story)
    print(OUTPUT)


if __name__ == "__main__":
    main()
